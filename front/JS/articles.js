    function renderArticles(year) {
        articlesGrid.innerHTML = '';
        const filteredArticles = year === 'all' ? articlesData : articlesData.filter(article => article.year === parseInt(year));
        
        filteredArticles.forEach((article, index) => {
            const articleBlock = document.createElement('div');
            articleBlock.classList.add('articles-block');
            articleBlock.dataset.id = article.id;
            articleBlock.innerHTML = `
                <p class="article-date">${article.date}</p>
                <h4 class="article-title">${article.title}</h4>
                <p class="article-short-text">${article.shortText}</p>
            `;
            articlesGrid.appendChild(articleBlock);
            setTimeout(() => {
                articleBlock.classList.add('visible');
            }, index * 100);
        });
    }

    timelineLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            timelineLinks.forEach(l => l.classList.remove('active'));
            link.classList.add('active');
            const year = link.dataset.year;
            loadArticles(year);
        });
    });

    function formatDateToRuFromISO(iso){
        if(!iso) return '';
        const p = iso.substring(0,10).split('-');
        if(p.length!==3) return iso;
        return p[2]+'.'+p[1]+'.'+p[0];
    }

    async function loadArticles(year){
        try{
            const url = year && year !== 'all' ? '/api/articles?year=' + encodeURIComponent(year) : '/api/articles';
            const res = await fetch(url, { cache: 'no-store', headers: { 'Cache-Control': 'no-cache' } });
            const items = await res.json();
            if(Array.isArray(items) && items.length > 0){
                articlesData = items.map(a => ({
                    id: a.id,
                    year: parseInt((a.published_at||'').substring(0,4))||0,
                    date: formatDateToRuFromISO(a.published_at),
                    title: a.title,
                    shortText: a.short_text,
                    fullText: a.full_text
                }));
            }
            renderArticles(year||'all');
        }catch(e){
            console.error('Failed to load articles', e);
            // Fallback to existing static articlesData
            renderArticles(year||'all');
        }
    }

    articlesGrid.addEventListener('click', (e) => {
        const block = e.target.closest('.articles-block');
        if (block) {
            const articleId = parseInt(block.dataset.id);
            const article = articlesData.find(a => a.id === articleId);

            if (article) {
                articleTitle.textContent = article.title;
                articleDate.textContent = article.date;
                articleFullText.innerHTML = article.fullText;

                modal.style.display = 'flex';
                setTimeout(() => {
                    modal.classList.add('open');
                }, 10);
            }
        }
    });

    closeBtn.addEventListener('click', () => {
        modal.classList.remove('open');
        setTimeout(() => {
            modal.style.display = 'none';
        }, 300);
    });

    window.addEventListener('click', (event) => {
        if (event.target === modal) {
            modal.classList.remove('open');
            setTimeout(() => {
                modal.style.display = 'none';
            }, 300);
        }
    });

